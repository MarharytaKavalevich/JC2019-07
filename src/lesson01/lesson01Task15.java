package lesson01;

public class lesson01Task15 {
    public static void main(String[] args) {

        for (int x = 1; x <=100; x ++) {
            System.out.print(x + " ");
        }
        System.out.println();

        int i = 1;
        while (i <= 100) {
            System.out.print(i + " ");
            i++;
        }
        System.out.println();

        int j = 1;
        do{
            System.out.print(j + " ");
            j++;
        } while (j <= 100);
    }
}
